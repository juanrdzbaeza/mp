package org.mp.sesion05;

import java.util.List;

/**
 * Class actions. It contains an action list.
 * 
 * @author Juan A. Rodriguez Baeza
 * @version 0.1 02/06/2015
 *
 */
public class Acciones{
	
	/**
	 * Attributes
	 * acciones: Action List
	 */
	private java.util.LinkedList<Accion> acciones;
	
	/**
	 * Class constructor. Actions builder. One LinkedList instance variable actions
	 * 
	 */
	public Acciones(){
		this.acciones = new java.util.LinkedList<Accion>();
	}
	
	/**
	 * Add a new actions to the list
	 * 
	 * @param a Action to add
	 */
	public void add(Accion a) {
		acciones.add(a);
	}
	
	/**
	 * Returns the action list
	 * @return action list
	 */
	public List<Accion> getAcciones() {
		return acciones;
	}

}
